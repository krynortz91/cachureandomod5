export class Producto{
    //Parametros de la clase 'Producto'
    constructor(imagen,nombre,codigo,descripcion,precio,cantidad,stock){
        this.imagen=imagen;
        this.nombre=nombre;
        this.codigo=codigo;
        this.descripcion=descripcion;
        this.precio=precio;
        this.cantidad=cantidad;
        this.stock=stock;
        
    }
}


export default Producto;
